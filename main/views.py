# Імпортуємо функцію render з Django для відображення HTML-шаблонів
from django.shortcuts import render

# ============================================================
# Головна сторінка
# ============================================================
def index(request):
    """
    Функція обробляє запит на головну сторінку сайту.
    Параметри:
    request: об'єкт HttpRequest, який містить дані запиту користувача

    Повертає:
    render: HTML-сторінку index.html з переданими даними
    """
    # Створюємо словник data, який передаємо у шаблон
    data = {
        'title': 'Головна сторінка',  # Заголовок сторінки
        'values': ['Some', 'Hello', '123'],  # Список значень, який можна відобразити на сторінці
        'obj': {  # Словник з даними про об'єкт
            'car': 'BWM',    # Марка автомобіля
            'age': 17,       # Вік
            'hobby': 'coding'  # Хобі
        }
    }

    # render() дозволяє передати дані у шаблон і повернути HTML-сторінку
    # 'main/index.html' — шлях до HTML-шаблону в папці templates/main/
    return render(request, 'main/index.html', data)


# ============================================================
# Сторінка "Про нас"
# ============================================================

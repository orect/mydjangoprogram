# news/admin.py

# ============================================================
# Імпорт модуля для реєстрації моделей у адмінці Django
# ============================================================
from django.contrib import admin

# Імпорт моделі Articles з нашого додатку
from .models import Articles

# ============================================================
# Реєстрація моделі Articles у панелі адміністратора
# ============================================================
# Це дозволяє бачити, додавати, редагувати і видаляти статті
# через стандартну адмін-панель Django
admin.site.register(Articles)

# ============================================================
# Імпорт необхідних модулів для створення маршрутів URL
# ============================================================
from django.contrib import admin  # Панель адміністратора Django
from django.urls import path, include  # path для маршрутів, include для підключення urls інших додатків

# ============================================================
# Головний список маршрутів URL для проєкту
# ============================================================
urlpatterns = [
    # URL для доступу до адмін-панелі
    # Коли користувач заходить на /admin/, відкривається стандартна адмінка Django
    path('admin/', admin.site.urls),

    # Підключення маршруту додатку main
    # '' — означає головну сторінку сайту
    # Django шукатиме маршрути у файлі main/urls.py
    path('', include('main.urls')),

    # Підключення маршруту додатку news
    # 'news/' — префікс для всіх сторінок новин
    # Django шукатиме маршрути у файлі news/urls.py
    path('news/', include('news.urls'))
]
